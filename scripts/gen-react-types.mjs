#!/usr/bin/env node
/**
 * Reads custom-elements.json and regenerates src/react.ts.
 * Invoked automatically by `npm run build` via the analyze step.
 */
import { readFileSync, writeFileSync } from 'fs';
import { resolve, dirname } from 'path';
import { fileURLToPath } from 'url';

const root = resolve(dirname(fileURLToPath(import.meta.url)), '..');
const manifest = JSON.parse(readFileSync(resolve(root, 'custom-elements.json'), 'utf8'));

const PRIMITIVES = new Set(['string', 'number', 'boolean', 'any', 'unknown', 'null', 'undefined']);

const elements = [];
// typeName → import path relative to src/  (e.g. ButtonVariant → './as-button.js')
const typeImports = new Map();

for (const mod of manifest.modules ?? []) {
  for (const decl of mod.declarations ?? []) {
    if (!decl.customElement || !decl.tagName) continue;

    const attrs = (decl.attributes ?? []).map(attr => ({
      name: attr.name,
      type: attr.type?.text ?? 'string',
    }));

    for (const attr of attrs) {
      if (!PRIMITIVES.has(attr.type)) {
        // mod.path is e.g. "src/as-button.ts" → "./as-button.js"
        const importPath = './' + mod.path.replace(/^src\//, '').replace(/\.ts$/, '.js');
        typeImports.set(attr.type, importPath);
      }
    }

    elements.push({ tagName: decl.tagName, attrs });
  }
}

// Group imports by path
const importsByPath = new Map();
for (const [typeName, path] of typeImports) {
  if (!importsByPath.has(path)) importsByPath.set(path, []);
  importsByPath.get(path).push(typeName);
}

const lines = [
  '// AUTO-GENERATED by scripts/gen-react-types.mjs — do not edit by hand.',
  '// Run `npm run build` to update after adding or changing components.',
  '',
  'export {};',
];

for (const [path, names] of importsByPath) {
  lines.push(`import type { ${names.join(', ')} } from '${path}';`);
}

lines.push(
  '',
  'type HTMLProps = {',
  '  id?: string;',
  '  class?: string;',
  '  className?: string;',
  '  style?: string | Record<string, string>;',
  '  slot?: string;',
  '  children?: unknown;',
  '  ref?: unknown;',
  '  key?: string | number;',
  '  onClick?: (event: MouseEvent) => void;',
  '  onFocus?: (event: FocusEvent) => void;',
  '  onBlur?: (event: FocusEvent) => void;',
  '  onKeyDown?: (event: KeyboardEvent) => void;',
  '  onKeyUp?: (event: KeyboardEvent) => void;',
  '  onMouseEnter?: (event: MouseEvent) => void;',
  '  onMouseLeave?: (event: MouseEvent) => void;',
  '  [attr: string]: unknown;',
  '};',
  '',
  'declare global {',
  '  namespace JSX {',
  '    interface IntrinsicElements {',
);

for (const { tagName, attrs } of elements) {
  lines.push(`      '${tagName}': HTMLProps & {`);
  for (const attr of attrs) {
    lines.push(`        '${attr.name}'?: ${attr.type};`);
  }
  lines.push('      };');
}

lines.push('    }', '  }', '}', '');

writeFileSync(resolve(root, 'src/react.ts'), lines.join('\n'), 'utf8');
console.log('[gen-react-types] wrote src/react.ts');
